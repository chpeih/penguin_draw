# -*- coding: utf-8 -*-
import turtle
kBezierStepNum = 15
kSpeed = 10

min_x = 1000000
max_x = -1000000
min_y = min_x
max_y = max_x
def Bezier(p1, p2, t):
	return p1 * (1-t) + p2 *t

def Bezier3(x2, y2, x3, y3, x4, y4):
	global min_x, max_x, min_y, max_y
	# 三阶贝塞尔曲线
	x1 = turtle.xcor()
	y1 = turtle.ycor()
	turtle.goto(x1, y1)
	turtle.pendown()
	for t in range(0, kBezierStepNum+1):
		t = 1.0 *t
		x = Bezier(Bezier(Bezier(x1, x2, t / kBezierStepNum), Bezier(x2, x3, t / kBezierStepNum), t / kBezierStepNum), Bezier(Bezier(x2, x3, t / kBezierStepNum), Bezier(x3, x4, t / kBezierStepNum), t / kBezierStepNum), t / kBezierStepNum)
		y = Bezier(Bezier(Bezier(y1, y2, t / kBezierStepNum), Bezier(y2, y3, t / kBezierStepNum), t / kBezierStepNum), Bezier(Bezier(y2, y3, t / kBezierStepNum), Bezier(y3, y4, t / kBezierStepNum), t / kBezierStepNum), t / kBezierStepNum)
		turtle.goto(x, y)
		min_x = min(min_x, x)
		max_x = max(max_x, x)
		min_y = min(min_y, y)
		max_y = max(max_y, y)
	turtle.penup()

def Bezier3_r(x2, y2, x3, y3, x4, y4):
	x = turtle.xcor()
	y = turtle.ycor()
	return Bezier3(x + x2, y + y2,
			x + x3, y + y3, x + x4, y + y4)

